from docx import Document

# Создаём документ
doc = Document()
doc.add_heading("Путешествия: Открывайте мир", level=1)

# Введение
doc.add_paragraph(
    "Путешествия — это один из лучших способов расширить кругозор, узнать о новых культурах и насладиться красотой "
    "нашей планеты. В каждом путешествии нас ждут незабываемые впечатления, будь то отдых на пляже, исследование "
    "древних руин или походы в горы."
)

# Раздел о популярных направлениях
doc.add_heading("Популярные направления для путешествий", level=2)

popular_destinations = [
    {"Место": "Мальдивы", "Описание": "Райские острова с белоснежными пляжами и кристально чистой водой."},
    {"Место": "Италия", "Описание": "Страна искусства, вкусной кухни и исторических достопримечательностей, таких как Колизей."},
    {"Место": "Япония", "Описание": "Сочетание древних традиций и высоких технологий, включая храмы и небоскрёбы."},
    {"Место": "Новая Зеландия", "Описание": "Изумительные природные ландшафты, от гор до озёр."},
    {"Место": "Южная Африка", "Описание": "Сафари, дикая природа и великолепный Кейптаун."}
]

for destination in popular_destinations:
    doc.add_heading(destination["Место"], level=3)
    doc.add_paragraph(destination["Описание"])

# Раздел с советами
doc.add_heading("Советы путешественникам", level=2)
doc.add_paragraph("1. Планируйте маршрут заранее, но оставьте место для спонтанности.")
doc.add_paragraph("2. Узнайте о культуре и традициях страны перед поездкой.")
doc.add_paragraph("3. Всегда имейте с собой копии документов и немного наличных.")
doc.add_paragraph("4. Оформите медицинскую страховку на время поездки.")
doc.add_paragraph("5. Не забывайте наслаждаться моментом и делать фотографии!")

# Заключение
doc.add_paragraph(
    "Путешествия делают нас богаче, наполняют нашу жизнь яркими красками и воспоминаниями. "
    "Каждое новое место — это ещё одна история, которую стоит открыть для себя."
)

# Сохранение документа
doc_file_path = "travel_info.doc"
doc.save(doc_file_path)

