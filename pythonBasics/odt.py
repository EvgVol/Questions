from docx import Document

# Создаём документ
doc = Document()
doc.add_heading("Автомобили: Информация и классификация", level=1)

# Введение
doc.add_paragraph(
    "Автомобили — это транспортные средства, используемые для перевозки пассажиров и грузов. "
    "Они делятся на множество категорий, начиная от легковых машин и заканчивая грузовиками и спорткарами. "
    "Ниже представлены данные о различных типах автомобилей, их характеристиках и популярности."
)

# Таблица данных
doc.add_heading("Типы автомобилей", level=2)
table = doc.add_table(rows=1, cols=4)
table.style = "Light Grid"

# Заголовки таблицы
header_cells = table.rows[0].cells
header_cells[0].text = "Тип автомобиля"
header_cells[1].text = "Средняя скорость (км/ч)"
header_cells[2].text = "Основное использование"
header_cells[3].text = "Пример модели"

# Данные таблицы
car_data = [
    ["Седан", "120-180", "Перевозка пассажиров", "Toyota Camry"],
    ["Кроссовер", "100-160", "Город и бездорожье", "Hyundai Tucson"],
    ["Спортивный автомобиль", "200-350", "Гонки и хобби", "Porsche 911"],
    ["Грузовик", "80-120", "Перевозка грузов", "Volvo FH"],
    ["Электромобиль", "120-250", "Экологичный транспорт", "Tesla Model S"]
]

# Заполняем таблицу
for car in car_data:
    row_cells = table.add_row().cells
    for i, value in enumerate(car):
        row_cells[i].text = value

# Заключение
doc.add_paragraph(
    "Автомобили продолжают развиваться с появлением новых технологий, таких как электродвигатели "
    "и системы автономного управления. Эти новшества не только делают машины удобнее и безопаснее, "
    "но и уменьшают их воздействие на окружающую среду."
)

# Сохранение документа
odt_file_path = "cars_info.odt"
doc.save(odt_file_path)

