from docx import Document

# Создаём документ
doc = Document()
doc.add_heading("Города: Информация и характеристики", level=1)

# Введение
doc.add_paragraph(
    "Города являются центрами культурной, экономической и социальной жизни. "
    "Каждый город уникален, имеет свои особенности, достопримечательности и роль в истории. "
    "Ниже представлена информация о некоторых известных городах мира."
)

# Таблица данных
doc.add_heading("Известные города", level=2)
table = doc.add_table(rows=1, cols=4)
table.style = "Light Grid"

# Заголовки таблицы
header_cells = table.rows[0].cells
header_cells[0].text = "Город"
header_cells[1].text = "Страна"
header_cells[2].text = "Население (млн)"
header_cells[3].text = "Известная достопримечательность"

# Данные таблицы
city_data = [
    ["Париж", "Франция", "11", "Эйфелева башня"],
    ["Токио", "Япония", "37", "Синдзюку"],
    ["Нью-Йорк", "США", "19", "Статуя Свободы"],
    ["Москва", "Россия", "12.6", "Красная площадь"],
    ["Сидней", "Австралия", "5.3", "Сиднейский оперный театр"]
]

# Заполняем таблицу
for city in city_data:
    row_cells = table.add_row().cells
    for i, value in enumerate(city):
        row_cells[i].text = value

# Заключение
doc.add_paragraph(
    "Города всегда играли важную роль в развитии цивилизации. "
    "Сегодня они остаются центрами инноваций, туризма и культурного обмена, "
    "собирая людей со всего мира."
)

# Сохранение документа в формате RTF
rtf_file_path = "/mnt/data/cities_info.rtf"
doc.save(rtf_file_path)

